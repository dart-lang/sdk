const keepTempKey = 'KEEP_TEMPORARY_DIRECTORIES';

Future<void> inTempDir(Future<void> Function(Uri tempUri) fun) async {
  final tempDir = await Directory.systemTemp.createTemp();
  // Deal with Windows temp folder aliases.
  final tempUri = Directory(
    await tempDir.resolveSymbolicLinks(),
  ).uri.normalizePath();
  try {
    await fun(tempUri);
  } finally {
    if (!Platform.environment.containsKey(keepTempKey) ||
        Platform.environment[keepTempKey]!.isEmpty) {
      try {
        await tempDir.delete(recursive: true);
      } on PathAccessException {
        if (Platform.isWindows) {
          // Don't fail on files being in use.
        } else {
          rethrow;
        }
      }
    }
  }
}

/// Runs a [Process].
///
/// If [logger] is provided, stream stdout and stderr to it.
///
/// If [captureOutput], captures stdout and stderr.
Future<RunProcessResult> runProcess({
  required Uri executable,
  List<String> arguments = const [],
  Uri? workingDirectory,
  Map<String, String>? environment,
  bool includeParentEnvironment = true,
  required Logger? logger,
  bool captureOutput = true,
  int expectedExitCode = 0,
  bool throwOnUnexpectedExitCode = false,
}) async {
  final printWorkingDir =
      workingDirectory != null && workingDirectory != Directory.current.uri;
  String quoteIfSpaced(String s) => s.contains(' ') ? '"$s"' : s;
  final commandString = [
    if (printWorkingDir) '(cd ${workingDirectory.toFilePath()};',
    ...?environment?.entries.map((entry) => '${entry.key}=${entry.value}'),
    quoteIfSpaced(executable.toFilePath()),
    ...arguments.map(quoteIfSpaced),
    if (printWorkingDir) ')',
  ].join(' ');
  logger?.info('Running `$commandString`.');

  final stdoutBuffer = StringBuffer();
  final stderrBuffer = StringBuffer();
  // On Windows, `CreateProcess` (used when `runInShell` is `false`) ignores
  // `workingDirectory` when resolving [executable]. Relative paths that
  // contain a directory component must be made absolute against
  // [workingDirectory] first; otherwise Process.start fails with
  // "The system cannot find the file specified".
  final resolvedExecutable = _resolveExecutablePath(
    executable: executable,
    workingDirectory: workingDirectory,
  );
  final process = await Process.start(
    resolvedExecutable.toFilePath(),
    arguments,
    workingDirectory: workingDirectory?.toFilePath(),
    environment: environment,
    includeParentEnvironment: includeParentEnvironment,
    // On Windows, only launch through `cmd.exe` when strictly necessary.
    //
    // `CreateProcess` (used when `runInShell` is `false`) can directly launch
    // `.exe`/`.com` executables and applies the correct command-line quoting,
    // so we avoid the shell for those: running them through `cmd.exe /c`
    // mangles the command line when more than one argument is quoted (cmd
    // strips the outer quotes when the line contains more than two quote
    // characters), which breaks any invocation whose executable and arguments
    // contain spaces.
    //
    // However, `CreateProcess` cannot resolve a bare command name to a
    // `.bat`/`.cmd` shim via `PATHEXT`, nor execute such a shim directly (for
    // example the executables generated by `dart install`). Those must go
    // through `cmd.exe`, so we opt into the shell for them.
    runInShell: Platform.isWindows && _needsShellOnWindows(executable),
  );

  final stdoutSub = process.stdout.listen((List<int> data) {
    try {
      final decoded = systemEncoding.decode(data);
      logger?.fine(decoded);
      if (captureOutput) {
        stdoutBuffer.write(decoded);
      }
    } catch (e) {
      logger?.warning('Failed to decode stdout: $e');
      stdoutBuffer.write('Failed to decode stdout: $e');
    }
  });
  final stderrSub = process.stderr.listen((List<int> data) {
    try {
      final decoded = systemEncoding.decode(data);
      logger?.severe(decoded);
      if (captureOutput) {
        stderrBuffer.write(decoded);
      }
    } catch (e) {
      logger?.severe('Failed to decode stderr: $e');
      stderrBuffer.write('Failed to decode stderr: $e');
    }
  });

  final (exitCode, _, _) = await (
    process.exitCode,
    stdoutSub.asFuture<void>(),
    stderrSub.asFuture<void>(),
  ).wait;
  final result = RunProcessResult(
    pid: process.pid,
    command: commandString,
    exitCode: exitCode,
    stdout: stdoutBuffer.toString(),
    stderr: stderrBuffer.toString(),
  );
  if (throwOnUnexpectedExitCode && expectedExitCode != exitCode) {
    throw ProcessException(
      executable.toFilePath(),
      arguments,
      "Full command string: '$commandString'.\n"
      "Exit code: '$exitCode'.\n"
      'For the output of the process check the logger output.',
    );
  }
  return result;
}

/// Resolves [executable] against [workingDirectory] when needed.
///
/// On Windows, [Process.start] does not resolve a relative [executable] against
/// [workingDirectory] (unlike POSIX, where the directory is changed before
/// exec). Relative paths that include a directory component are therefore made
/// absolute here. Bare command names (no separators) are left unchanged so
/// `PATH` / `PATHEXT` lookup still works.
Uri _resolveExecutablePath({
  required Uri executable,
  required Uri? workingDirectory,
}) {
  final executablePath = executable.toFilePath();
  if (workingDirectory == null) return executable;

  if (executable.isAbsolute) return executable;
  // Bare command name — leave for PATH / PATHEXT resolution.
  if (!executablePath.contains(r'\') && !executablePath.contains('/')) {
    return executable;
  }
  return workingDirectory.resolveUri(executable);
}

/// Whether [executable] must be launched through `cmd.exe` on Windows.
///
/// `CreateProcess` can only directly launch `.exe`/`.com` binaries. Anything
/// else (a `.bat`/`.cmd` shim, or a bare command name that resolves to one via
/// `PATHEXT`) has to be run through the shell.
bool _needsShellOnWindows(Uri executable) {
  final path = executable.toFilePath();
  final lastSeparator = path.lastIndexOf(RegExp(r'[\\/]'));
  final fileName = lastSeparator == -1
      ? path
      : path.substring(lastSeparator + 1);
  final dot = fileName.lastIndexOf('.');
  // No extension (e.g. a bare command name resolved via `PATHEXT`).
  if (dot <= 0) return true;
  final extension = fileName.substring(dot).toLowerCase();
  return extension != '.exe' && extension != '.com';
}

/// Drop in replacement of [ProcessResult].
class RunProcessResult {
  final int pid;

  final String command;

  final int exitCode;

  final String stderr;

  final String stdout;

  RunProcessResult({
    required this.pid,
    required this.command,
    required this.exitCode,
    required this.stderr,
    required this.stdout,
  });

  @override
  String toString() =>
      '''command: $command
exitCode: $exitCode
stdout: $stdout
stderr: $stderr''';
}

Future<void> copyDirectory(Directory source, Directory target) async {
  await target.create(recursive: true);
  await for (final entity in source.list(recursive: false)) {
    final newPath = path.join(target.path, path.basename(entity.path));
    if (entity is Directory) {
      await copyDirectory(entity, Directory(newPath));
    } else if (entity is File) {
      await entity.copy(newPath);
    }
  }
}

Future<void> copyTestProjects(
  Uri copyTargetUri,
  Logger logger,
  Uri packageLocation,
  Uri sdkRoot,
  bool usePubWorkspace,
) async {
  // Reuse the test projects from `pkg:native`.
  final testProjectsUri = packageLocation.resolve('test_data/');
  final manifestUri = testProjectsUri.resolve('manifest.yaml');
  final manifestFile = File.fromUri(manifestUri);
  final manifestString = await manifestFile.readAsString();
  final manifestYaml = loadYamlDocument(manifestString);
  final manifest = [
    for (final path in manifestYaml.contents as YamlList) Uri(path: path),
  ];
  final filesToCopy = manifest
      .where(
        (e) =>
            !(e.pathSegments.last.startsWith('pubspec') &&
                e.pathSegments.last.endsWith('.yaml')),
      )
      .toList();
  final pubspecPaths = manifest
      .where(
        (e) =>
            e.pathSegments.last.startsWith('pubspec') &&
            e.pathSegments.last.endsWith('.yaml'),
      )
      .toList();

  for (final pathToCopy in filesToCopy) {
    final sourceFile = File.fromUri(testProjectsUri.resolveUri(pathToCopy));
    final targetUri = copyTargetUri.resolveUri(pathToCopy);
    final targetDirUri = targetUri.parent;
    final targetDir = Directory.fromUri(targetDirUri);
    if (!(await targetDir.exists())) {
      await targetDir.create(recursive: true);
    }
    await sourceFile.copy(targetUri.toFilePath());
  }
  final dependencyOverrides = {
    for (final package in [
      'code_assets',
      'data_assets',
      'hooks',
      'native_toolchain_c',
      'record_use',
    ])
      package: {
        'path': sdkRoot
            .resolve('third_party/pkg/native/pkgs/$package/')
            .toFilePath(),
      },
    for (final package in [
      '_fe_analyzer_shared',
      'analyzer', // package:analyzer depends on package:meta.
      'meta',
    ])
      package: {'path': sdkRoot.resolve('pkg/$package/').toFilePath()},
  };
  final userDefinesWorkspace = {};
  for (final pubspecPath in pubspecPaths) {
    final sourceFile = File.fromUri(testProjectsUri.resolveUri(pubspecPath));
    final targetUri = copyTargetUri.resolveUri(pubspecPath);
    final sourceString = await sourceFile.readAsString();
    final pubspec = YamlEditor(sourceString);
    final pubspecRead = loadYamlNode(sourceString) as Map;
    if (!usePubWorkspace) {
      if (pubspecRead['resolution'] != null) {
        pubspec.remove(['resolution']);
      }
      pubspec.update(['dependency_overrides'], dependencyOverrides);
    } else {
      final userDefines = pubspecRead['hooks']?['user_defines'];
      if (userDefines is Map) {
        // Remove the user defines from the root package pubspec.
        pubspec.remove(['hooks', 'user_defines']);

        // Add the user defines to the workspace pubspec.
        // But make sure to rewrite relative paths to point to the right place.
        // Deep-copy the map because the read map is unmodifiable.
        for (final MapEntry(:key, :value) in userDefines.entries) {
          final packageName = key;
          final defines = value as Map;
          userDefinesWorkspace[packageName] = <String, Object?>{};
          for (final MapEntry(:key, :value) in defines.entries) {
            if (value == 'assets/data.json') {
              // We're constructing a workspace, so the paths in the workspace pubspec must point to the right place.
              userDefinesWorkspace[packageName]![key] =
                  '$packageName/assets/data.json';
            } else {
              userDefinesWorkspace[packageName]![key] = value;
            }
          }
        }
      }
    }
    final modifiedString = pubspec.toString();
    await File.fromUri(targetUri).writeAsString(modifiedString);
  }
  if (usePubWorkspace) {
    final workspacePubspec = YamlEditor('');
    workspacePubspec.update([], {
      'name': 'my_pub_workspace',
      'environment': {'sdk': '>=3.7.0 <4.0.0'},
      'workspace': [
        for (final pubspec in pubspecPaths)
          if (!pubspec.toFilePath().contains('version_skew'))
            pubspec.toFilePath().replaceAll('pubspec.yaml', ''),
      ],
      'dependency_overrides': dependencyOverrides,
      'hooks': {'user_defines': userDefinesWorkspace},
    });
    final pubspecUri = copyTargetUri.resolve('pubspec.yaml');
    await File.fromUri(pubspecUri).writeAsString(workspacePubspec.toString());
  }

  // If we're copying `my_native_library/` we need to simulate that its
  // native assets are pre-built
  final myNativeLibraryUri = copyTargetUri.resolve('my_native_library/');
  if (await Directory(myNativeLibraryUri.toFilePath()).exists()) {
    await runPubGet(workingDirectory: myNativeLibraryUri, logger: logger);
    await runDart(
      arguments: ['tool/native.dart', 'build'],
      workingDirectory: myNativeLibraryUri,
      logger: logger,
    );
  }
}

Future<void> runPubGet({
  required Uri workingDirectory,
  required Logger logger,
}) async {
  final result = await runDart(
    arguments: ['pub', 'get'],
    workingDirectory: workingDirectory,
    logger: logger,
  );
  expect(result.exitCode, 0);
}

