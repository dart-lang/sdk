import 'dart:convert';
import 'dart:io';

import 'package:path/path.dart';
import 'package:test/test.dart';
import 'package:test_reflective_loader/test_reflective_loader.dart';

import '../../test/utils/lsp_protocol_extensions.dart';
import '../support/plugin_mixin.dart';
import 'integration_tests.dart';

void main() {
  defineReflectiveSuite(() {
    defineReflectiveTests(WorkspaceAnalysisCompleteTest);
  });
}

@reflectiveTest
class WorkspaceAnalysisCompleteTest
    extends AbstractLspAnalysisServerIntegrationTest
    with PluginMixin {
  Future<void> test_plugin_invalidPath() async {
    // Update the analysis_options file to have a plugin with a path that
    // doesn't exist.
    newFile(analysisOptionsPath, '''
plugins:
  my_plugin:
    path: ${jsonEncode(join(projectFolderPath, 'missing_plugin'))}
''');

    newFile(mainFilePath, '');
    await initialize();

    // We don't expect any diagnostics (plugin is invalid),
    // but we do expect this request to complete regardless.
    await workspaceAnalysisComplete();
  }

  Future<void> test_plugin_nameMismatch() async {
    createPlugins([alwaysProduceDiagnosticPlugin]);

    // Update the analysis_options file to have the wrong name for the plugin.
    newFile(
      analysisOptionsPath,
      File(analysisOptionsPath)
          .readAsStringSync()
          .replaceAll('${alwaysProduceDiagnosticPlugin.name}:', 'foo_plugin:'),
    );

    newFile(mainFilePath, '');
    await initialize();

    // We don't expect any diagnostics (plugin is invalid),
    // but we do expect this request to complete regardless.
    await workspaceAnalysisComplete();
  }

  Future<void> test_plugin_throws_asynchronously() async {
    createPlugins([throwAsynchronouslyPlugin]);

    newFile(mainFilePath, '');
    await initialize();

    // We don't expect any diagnostics (plugin is invalid),
    // but we do expect this request to complete regardless.
    await workspaceAnalysisComplete();
  }

  Future<void> test_plugin_throws_duringAnalysis() async {
    createPlugins([throwDuringAnalysisPlugin]);

    newFile(mainFilePath, '');
    await initialize();

    // We don't expect any diagnostics (plugin is invalid),
    // but we do expect this request to complete regardless.
    await workspaceAnalysisComplete();
  }

  Future<void> test_plugin_throws_inConstructor() async {
    createPlugins([throwInConstructorPlugin]);

    newFile(mainFilePath, '');
    await initialize();

    // We don't expect any diagnostics (plugin is invalid),
    // but we do expect this request to complete regardless.
    await workspaceAnalysisComplete();
  }

  Future<void> test_plugin_valid() async {
    createPlugins([alwaysProduceDiagnosticPlugin]);

    newFile(mainFilePath, '');
    await initialize();
    await workspaceAnalysisComplete();

    var diagnosticMessages = diagnostics[mainFileUri]?.map(
      (diagnostic) => diagnostic.message.asString,
    );
    expect(diagnosticMessages, contains('A diagnostic generated by a plugin.'));
  }
}
