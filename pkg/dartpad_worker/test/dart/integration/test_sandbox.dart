@TestOn('browser')
library;

import '../../integration_harness.dart';

void main() {
  testDartIntegration('sandbox can run code', (ctx) async {
    await ctx.sandbox.loadModule(code: exampleDdcModule());

    await ctx.sandbox.runMain(Uri.parse('file:///workspace/pad_1/main.dart'));

    await ctx.checkConsole((m) => m.contains('Hello World'));
  });

  testDartIntegration('sandbox handles unhandled error', (ctx) async {
    final errorModule = exampleDdcModule(r'''
      setTimeout(function() {
        throw new Error("uncaught error in sandbox");
      }, 0);
      ''');

    await ctx.sandbox.loadModule(code: errorModule);

    final errorFuture = ctx.sandbox.onError.first;

    await ctx.sandbox.runMain(Uri.parse('file:///workspace/pad_1/main.dart'));

    await check(errorFuture).completes(
      (r) => r
          .has((r) => r.message, 'message')
          .contains('uncaught error in sandbox'),
    );
  });

  testDartIntegration('sandbox handles unhandled promise rejection', (
    ctx,
  ) async {
    final rejectionModule = exampleDdcModule(r'''
      setTimeout(function() {
        Promise.reject(new Error("unhandled rejection in sandbox"));
      }, 0);
      ''');

    await ctx.sandbox.loadModule(code: rejectionModule);

    final rejectionFuture = ctx.sandbox.onUnhandledRejection.first;

    await ctx.sandbox.runMain(Uri.parse('file:///workspace/pad_1/main.dart'));

    await check(rejectionFuture).completes(
      (r) => r
          .has((r) => r.message, 'message')
          .contains('unhandled rejection in sandbox'),
    );
  });

  testDartIntegration('sandbox handles extension event', (ctx) async {
    final extensionModule = exampleDdcModule(r'''
      self.$emitDebugEvent("my.custom.event", '{"foo": "bar"}');
      ''');

    await ctx.sandbox.loadModule(code: extensionModule);

    final eventFuture = ctx.sandbox.onExtensionEvent.first;

    await ctx.sandbox.runMain(Uri.parse('file:///workspace/pad_1/main.dart'));

    await check(eventFuture).completes((r) {
      r.has((e) => e.kind, 'kind').equals('my.custom.event');
      r.has((e) => e.data, 'data').deepEquals({'foo': 'bar'});
    });
  });
}

